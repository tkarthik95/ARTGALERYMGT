  );

-- CUSTOMER
   CREATE TABLE CUSTOMER(
    CUSTID VARCHAR(10) PRIMARY KEY,
    GID VARCHAR(10),
    CONSTRAINT FK_CUSTOMER_GID FOREIGN KEY (GID) REFERENCES GALLERY(GID)
